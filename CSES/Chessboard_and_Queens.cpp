vector<string> grid(8);
vector<bool> rows(8), cols(8), diag1(15), diag2(15);
int ans = 0;

void complete_search(int row){
    if(row == 8){
        ans++;
        return;
    }

    for(int col = 0; col < 8; col++){
        if(grid[row][col] == '*') continue;
        if(!rows[row] && !cols[col] && !diag1[row - col + 7] && !diag2[row + col]){
            rows[row] = cols[col] = diag1[row - col + 7] = diag2[row + col] = true;
            complete_search(row + 1);
            rows[row] = cols[col] = diag1[row - col + 7] = diag2[row + col] = false;
        }
    }
}

void solve(int num_tc)
{
    for(int i = 0; i < 8; i++){
        cin >> grid[i];
    }
    complete_search(0);
    cout << ans << endll;
}

