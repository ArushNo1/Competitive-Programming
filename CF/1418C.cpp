#define mineq(a, b) a = min(a, b)

void solve(int num_tc)
{
    int n;
    cin >> n;
    vi bosses(n);
    fillv(bosses, n);

    vvi skippoints(n + 1, vi(2, 1e9));
    skippoints[0][0] = 0;
    //0: friend, 1: you
    //skippoints[i][j] is number of skip points required to reach boss i during turn j

    for(int i = 0; i < n; i++){
        mineq(skippoints[i + 1][1], skippoints[i][0] + bosses[i]);
        mineq(skippoints[i + 1][0], skippoints[i][1]);
        if(i <= n - 2){ 
            mineq(skippoints[i + 2][1], skippoints[i][0] + bosses[i] + bosses[i + 1]);   
            mineq(skippoints[i + 2][0], skippoints[i][1]);
        }
    }
    cout << min(skippoints[n][0], skippoints[n][1]) << endll;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);  

    ll T = 1;
    cin >> T;
    for(ll t = 0; t < T; t++){
        solve(t+1);
    }
}
