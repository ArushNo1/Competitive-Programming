void solve(int num_tc)
{
    int n, m;
    cin >> n >> m;
    vvi adj(n);
    for(int i = 0; i < m; i++){
        int u, v;
        cin >> u >> v;
        u--; v--;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    vb visited(n);
    stack<pair<int, int>> q;
    q.push({0, 0});
    visited[0] = true;
    vi even;
    vi odd;
    while(!q.empty()){
        auto [u, d] = q.top();
        q.pop();
        if(d % 2 == 0){ even.push_back(u);}
        else{ odd.push_back(u); }
        for(int v : adj[u]){
            if(!visited[v]){
                visited[v] = true;
                q.push({v, d + 1});
            }
        }
    }
    if(even.size() > odd.size()){
        swap(even, odd);
    }
    cout << even.size() << endll;
    for(int node : even){
        cout << node + 1 << " ";
    }
    cout << endll;
}

