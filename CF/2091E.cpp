bool is_prime(int n) {
    if (n == 2) return true;
    if (n % 2 == 0) return false;
    for (int i = 3; i * i <= n; i += 2) {
        if (n % i == 0) return false;
    }
    return true;
}

void solve(int num_tc)
{
    ll n;
    cin >> n;
    vector<bool> prime(n + 2, true);
    prime[0] = prime[1] = false;
    ll ans = 0;
    for(ll i = 2; i <= n; i++){
        if(prime[i]){
            ans += n / i;
        }
        for(ll j = i * i; j <= n; j += i){
            prime[j] = false;
        }
    }
    
    cout << ans << endll;
}

