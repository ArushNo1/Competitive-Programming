int N, M, K;
vector<vector<pair<int, int>>> adj{};
vector<bool> vis{};

void dfs(ll n, ll T)
{
    vis[n] = true;
    for(pair<int, int> p : adj[n])
    {
        if(!vis[p.first] && p.second >= T) dfs(p.first, T);
    }
}

int getNumCs(ll T)
{
    ll numC = 0;

    vis = vector<bool>(N, false);
    for(ll i = 0; i < N; i++)
    {
        if(!vis[i]) 
        {
            dfs(i, T);
            numC++;
        }
    }
    return numC;
}

void solve(int num_tc)
{
    cin >> N >> M >> K;
    adj = vector<vector<pair<int, int>>>(N);

    for(int i = 0; i < M; i++)
    {
        int u, v, w; cin >> u >> v >> w;
        u--; v--;
        adj[u].push_back({v, w});
        adj[v].push_back({u, w});
    }

    ll hi = 1e18, lo = 0;
    while(hi > lo)
    {
        ll mid = (hi+lo)/2;
        

        if(getNumCs(mid) < K)
        {
            lo = mid + 1;
        }
        else 
        {
            hi = mid;
        }
    }

    for(int k = hi - 5; k < lo + 5; k++)
    {
        if(getNumCs(k) == K)
        {
            cout << k << endll;
            return;
        }
    }

    cout << "IMPOSSIBLE" << endll;
}

